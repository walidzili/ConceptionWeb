                    L’histoire de l’Armada débute en 1989. Cette date marque le début de cette aventure aujourd’hui âgée de 30 ans. La première édition de l’Armada de Rouen porte le nom des Voiles de la Liberté. A cette époque, c’est la première édition au monde de rassemblement des plus grands voiliers. Elle s’est déroulée du 9 au 16 juillet 1989 et a célébré le bicentenaire de la Révolution française et la Déclaration des droits de l’Homme et du Citoyen.
