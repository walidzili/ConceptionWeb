                <p>
                    <strong>1994</strong> 
                    la deuxième édition
                    Après le succès de la première édition,  l’Armada revient en 1994 pour une seconde édition intitulée « l’Armada de la liberté ». Ce deuxième grand rassemblement de voiliers a permis de commémorer le cinquantième anniversaire du débarquement des alliés en Normandie.
                    Cette édition sera suivie par près de 7 millions de visiteurs venus pendant 8 jours. En effet,  du 10 au 17 juillet 1994, les visiteurs ont admiré la cinquantaine de bateaux présents au programme : des grands voiliers et navires de guerre ainsi que de nombreux vieux gréements amarrés tout le long des quais de Seine à Rouen.

                    La grande parade sur les bords de Seine rassemblera même 2 millions de spectateurs émerveillés à elle seule. L’évènement s’installe dans le paysage normand et devient un rendez-vous à ne pas rater.
