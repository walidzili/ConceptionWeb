                <p>
                    <strong>2003</strong>
                    la quatrième édition
                    Quatre ans après la dernière édition, l’Armada fait son retour du 28 juin au 6 juillet 2003. Le rendez-vous est toujours aussi suivi par le public avec plus de 9 millions de visiteurs présents sur les quais de Seine.

                    La Grande Parade devient le moment le plus attendu de l’Armada. Ce sont près de 2 millions de spectateurs qui se rassemblent sur les bords de Seine pour participer à la descente des bateaux et admirer les cordages, les voiles carrées et autres voilures naviguer sur le fleuve jusqu’à l’estuaire.

                    De nombreux reportages dans les médias sont réalisés (56 chaines de télévisions dans plus de 21 pays) permettent à l’Armada de devenir un événement d’envergure internationale. La manifestation séduit des touristes du monde entier.
                </p>
