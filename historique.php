                <p>
                    <strong>2008</strong>
                    la cinquième édition
                    L’édition 2008 de l’Armada de Rouen s’est déroulée du 5 au 14 juillet 2008. Elle a réalisé un record de fréquentation avec près de 11 millions de visiteurs. Le public venu assisté à l’événement profite également des nombreuses visites rouennaises comme le Gros-Horloge ou le Musée des Beaux-Arts.

                    A l’occasion de cette édition, les festivités se multiplient et des nouveautés apparaissent comme l’élection demoiselle Armada. L’association internationale « Sail Training International », qui organise des courses de grands voiliers parraine l’événement. Rouen devient la ville de départ du prologue de la «Tall Ships Races 2008″.

                    L’autre nouveauté de cette Armada est la mise en service du pont Gustave-Flaubert. Il propose un spectacle inédit aux visiteurs : voir les plus grands voiliers du monde passer sous le tablier de ce géant de béton.
