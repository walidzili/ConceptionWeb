                <p>
                    <strong>1999</strong>
                    la troisième édition
                    La 3ème édition de l’Armada est organisée en 1999. Nommée « l’Armada du siècle », elle permet à une cinquantaines de bateaux et voiliers de faire escale à Rouen du  9 au 19 juillet 1999. Parmi eux, il y a les plus prestigieux et les plus grands au monde mais aussi des bâtiments de guerre et de vieux gréements. L’événement rassemble une trentaine de pays comme la Turquie, la Russie ou la Pologne.

                    Ce troisième rendez-vous est une véritable marée humaine avec 10 millions de visiteurs pendant ces 10 jours de festivités. Les animations autour de l’événement se font de plus en plus nombreuses avec les concerts, les feux d’artifice ou encore le Grande Pagaille.
                </p>
